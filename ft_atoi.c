#include "printf.h"

int	ft_atoi(char *s)
{
	long unsigned	ret;

	ret = 0;
	if (!s)
		return (0);
	while (*s >= '0' && *s <= '9')
	{
		ret = ret * 10 + *s - 48;
		s++;
	}
	return (ret);
}
